    public function findCommentsByQuestion($question){

        $em = $this->getEntityManager();

        $query = $em->createQuery(
            'SELECT comment, p
             FROM App\Entity\comment as comment
             JOIN comment.question as p
             WHERE comment.question = :question
             ORDER BY comment.validateComment DESC'
        )->setParameter('question', $question);
    
        // returns un tableau de Movie (objets)
        return $query->execute();
    }

